    }
	
	    /**
     * @return mixed
     */
    public function getImageProfile()
    {
        return $this->imageProfile;
    }

    /**
     * @param mixed $imageProfile
     */
    public function setImageProfile($imageProfile): void
    {
        $this->imageProfile = $imageProfile;
    }

}
